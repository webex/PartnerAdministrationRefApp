package com.cisco.model;

import java.util.List;

public class WholesaleSubscriberDetails {

    private String customerId;
    private String email;
    private String firstName;
    private String lastName;
    private String phoneNumber;
    private String extension;
    private String locationId;
    private List<Object> packages;

    public String getCustomerId() {
        return customerId;
    }

    public void setCustomerId(final String customerId) {
        this.customerId = customerId;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(final String email) {
        this.email = email;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(final String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(final String lastName) {
        this.lastName = lastName;
    }

    public void setPhoneNumber(final String phoneNumber) { this.phoneNumber = phoneNumber; }

    public String getPhoneNumber() { return phoneNumber; }

    public String getExtension() {
        return extension;
    }

    public void setExtension(final String extension) {
        this.extension = extension;
    }

    public String getLocationId() {
        return locationId;
    }

    public void setLocationId(final String locationId) {
        this.locationId = locationId;
    }

    public List<Object> getPackages() {
        return packages;
    }

    public void setPackages(final List<Object> packages) {
        this.packages = packages;
    }
}
